# Control Flow Statements in Python

# 1. if Statement
age = 20
if age >= 18:
    print("Farooq, you are an adult!")

# 2. if-else Statement
age = 16
if age >= 18:
    print("Farooq, you can vote.")
else:
    print("Farooq, you are too young to vote.")

# 3. if-elif-else Statement
marks = 85
if marks >= 90:
    print("Farooq, you got an A+")
elif marks >= 75:
    print("Farooq, you got an A")
else:
    print("Farooq, keep trying!")
