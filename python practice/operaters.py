#Arithmetic Operators in Python  

a = 10
b = 3

# Addition
print(a + b)    # 13

# Subtraction
print(a - b)    # 7

# Multiplication
print(a * b)    # 30

# Division (float)
print(a / b)    # 3.3333...

# Floor Division (integer)
print(a // b)   # 3

# Modulus (remainder)
print(a % b)    # 1

# Exponent (power)
print(a ** b)   # 1000



# comparison operaters in python
# return true or false

a = 5
b = 8

print(a == b)   # False
print(a != b)   # True
print(a > b)    # False
print(a < b)    # True
print(a >= b)   # False
print(a <= b)   # True



# logical operaters in python 
#combine boolean experessions

x = True
y = False

print(x and y)   # False
print(x or y)    # True
print(not x)     # False

